// If dan Else → untuk membuat percabangan berdasarkan kondisi.
// Else If → untuk menangani lebih dari satu kondisi.
// Switch Case → untuk kondisi bercabang banyak yang lebih rapi.
// Looping (Perulangan) → seperti for, while, dan do while untuk mengulang eksekusi kode.
// Break & Continue → untuk mengontrol jalannya perulangan.

void main() {
  for (int i = 1; i <= 10; i++) {
    if (i == 5) {
      continue;
    }
    print(i);
  }
}
