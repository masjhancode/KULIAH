// Operator Aritmatika → seperti +, -, *, /, % untuk operasi matematika dasar.
// Operator Perbandingan → seperti ==, !=, >, <, >=, <= untuk membandingkan dua nilai.
// Operator Logika → seperti &&, ||, ! untuk menggabungkan ekspresi logis.
// Operator Penugasan → seperti =, +=, -=, *= untuk memberikan atau memperbarui nilai pada variabel.
// Operator Increment & Decrement → ++ dan -- untuk menambah atau mengurangi nilai variabel dengan cepat.
// Operator Khusus Dart → misalnya ?? untuk null-aware operator, yang akan sering kita gunakan saat membuat aplikasi mobile.

void main(List<String> args) {
  for (int i = 1; i < 10; i--) {
    print(i);
  }
}
